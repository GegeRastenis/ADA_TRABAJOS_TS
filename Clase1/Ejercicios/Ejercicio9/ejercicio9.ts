/*Ejercicio 9: Comparaciones lógicas
Declara dos variables booleanas y realiza una comparación lógica (como AND, 
OR). Imprime el resultado*/

let primerDato: boolean = true; 
let segundoDato: boolean = false; 

let resultadoAnd: boolean = primerDato && segundoDato; 
console.log(`El resultado de esta operacion booleana es: ${resultadoAnd}`);

let resultadoOr: boolean = primerDato || segundoDato; 
console.log(`El resultado de esta operacion booleana es: ${resultadoOr}`);

